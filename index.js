
//JavaScript program to swap two variables


let x = parseInt(prompt("Enter first number"))  //here we take first input from user
let y = parseInt(prompt("Enter second number"))  //here we take second input from user

x = x + y   // here add both the values of x and y
y = x - y   // then we substract y value from total value then we get x value and that value assign to(y)
x = x - y   // then we substract x value from total value then we get y value and that value assign to(x)

console.log("After swap x is:  "+ x)
console.log("After swap y is:  "+ y)

document.write("After swap x is:   "+ x) ;
